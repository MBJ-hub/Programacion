    private void CrearEmpleado(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CrearEmpleado
        if (this.dialogo.mostarar() == DialogoEmpleado.ACEPATR) {
            this.tdni.setText(dialogo.getDni());
            this.tnombre.setText(dialogo.getNombre());
            this.tsalario.setText((dialogo.getSalario()).toString());
            this.ttipo.setSelectedItem(dialogo.getTipo());
            this.thoras.setValue(dialogo.getHorass());
            controlador.crearEmpleado();
            this.actualizarTabla();
        }

    }//GEN-LAST:event_CrearEmpleado

