    private void btnborrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnborrarActionPerformed
        // TODO add your handling code here:
        if(this.tDni)
        if (JOptionPane.showConfirmDialog(this, "¿Estas seguro?") == JOptionPane.OK_OPTION) {
            try {
                controlador.eliminarEmpleado();
                limpiarCampos();
            } catch (dniExepcion ex) {
                Logger.getLogger(Ventana.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.actualizarTabla();
        }
    }//GEN-LAST:event_btnborrarActionPerformed

    private void abrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_abrirActionPerformed
        // TODO add your handling code here:

        if (this.selectorFicheros.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            controlador.cargarEmpleados();
        }
    }//GEN-LAST:event_abrirActionPerformed

    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        // TODO add your handling code here:
        String dni = JOptionPane.showInputDialog(this, "Introduzca El empleado a buscar");
        if (dni != null) {
            try {
                limpiarCampos();
                mostrarDni(dni);
                controlador.buscarEmpleado();
            } catch (dniExepcion ex) {
                Logger.getLogger(Ventana.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_buscarActionPerformed

